package com.iparhan.financial.entity;

public class Historical {

	private int hid;
	private int tuser_Id;
	private int company_Id;
	private String company_name;
	private String hist_time;
	

	public int getHid() {
		return hid;
	}
	public void setHid(int hid) {
		this.hid = hid;
	}
	public int getTuser_Id() {
		return tuser_Id;
	}
	public void setTuser_Id(int tuser_Id) {
		this.tuser_Id = tuser_Id;
	}
	public int getCompany_Id() {
		return company_Id;
	}
	public void setCompany_Id(int company_Id) {
		this.company_Id = company_Id;
	}
	public String getCompany_name() {
		return company_name;
	}
	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}
	public String getHist_time() {
		return hist_time;
	}
	public void setHist_time(String hist_time) {
		this.hist_time = hist_time;
	}
	public Historical(int hid, int tuser_Id, int company_Id, String company_name, String hist_time) {
		super();
		this.hid = hid;
		this.tuser_Id = tuser_Id;
		this.company_Id = company_Id;
		this.company_name = company_name;
		this.hist_time = hist_time;
	}
	public Historical() {
		super();
	}
	 
	
}
