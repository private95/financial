package com.iparhan.financial.search.entity;


/**
 * 理财百科搜索业务——企业信息
 * @author Iparhan
 *
 */
public class CompanySearch {
	
	private String id;
	private String name; //企业名称
	private String personName;//核心人物姓名
	private String product_count;//产品数目
	private String establishTime; //成立日期
	private String cumulative_nav;// 累计收益
	private String production; //核心产品
	
	private String fund_shortName; //基金简称
	private String fundCompany;    //基金公司
	private Double nav;            //最新净值
	private Double topOneMonth;    //最新一个月
	private String price_date;     //最新净值日期
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPersonName() {
		return personName;
	}
	public void setPersonName(String personName) {
		this.personName = personName;
	}
	public String getProduct_count() {
		return product_count;
	}
	public void setProduct_count(String product_count) {
		this.product_count = product_count;
	}
	public String getEstablishTime() {
		return establishTime;
	}
	public void setEstablishTime(String establishTime) {
		this.establishTime = establishTime;
	}
	public String getCumulative_nav() {
		return cumulative_nav;
	}
	public void setCumulative_nav(String cumulative_nav) {
		this.cumulative_nav = cumulative_nav;
	}
	public String getProduction() {
		return production;
	}
	public void setProduction(String production) {
		this.production = production;
	}
	public String getFund_shortName() {
		return fund_shortName;
	}
	public void setFund_shortName(String fund_shortName) {
		this.fund_shortName = fund_shortName;
	}
	public String getFundCompany() {
		return fundCompany;
	}
	public void setFundCompany(String fundCompany) {
		this.fundCompany = fundCompany;
	}
	public Double getNav() {
		return nav;
	}
	public void setNav(Double nav) {
		this.nav = nav;
	}
	public Double getTopOneMonth() {
		return topOneMonth;
	}
	public void setTopOneMonth(Double topOneMonth) {
		this.topOneMonth = topOneMonth;
	}
	public String getPrice_date() {
		return price_date;
	}
	public void setPrice_date(String price_date) {
		this.price_date = price_date;
	}
	@Override
	public String toString() {
		return "CompanySearch [id=" + id + ", name=" + name + ", personName=" + personName + ", product_count="
				+ product_count + ", establishTime=" + establishTime + ", cumulative_nav=" + cumulative_nav
				+ ", production=" + production + ", fund_shortName=" + fund_shortName + ", fundCompany=" + fundCompany
				+ ", nav=" + nav + ", topOneMonth=" + topOneMonth + ", price_date=" + price_date + "]";
	}
	

}
